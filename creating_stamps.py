import turtle

turtle.shape('turtle')
finn = turtle.clone()
finn.shape('circle')
finn.goto(100,0)
finn.goto(100,100)
finn.goto(0,100)
finn.goto(0,0)
charlie = turtle.clone()
charlie.goto(-100,0)
charlie.goto(-50,100)
charlie.goto(0,0)
finn.penup()
finn.goto(100,100)
finn.goto(300,300)
finn.stamp()
finn.goto(100,100)
turtle.clearstamps()

turtle.mainloop()
